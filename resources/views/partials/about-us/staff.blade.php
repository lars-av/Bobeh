  <h2>Approach</h2>
  <div class="approach-text">
    <div class="text">
    <p>Our staff group is composed interdisciplinary of educators, occupational therapists, teachers and health professionals who work from a common interdisciplinary starting point. We use the KRAP method as a basic framework for our pedagogical work and documentation.</p>

    <p>The method works both as a pedagogical toolbox and more generally as a human vision, where we believe that everyone does the best they can based on their premise. We generally want the culture of our departments to be characterized by being appreciative and resource focused.
