<h3>Management</h3>
<p style ="max-width: 65%; line-height: 1.7rem;"> At Bo- og Behandlingscenter Blokhus, our goal is to be really able to help and support our residents in their everyday lives, so that the individual resident's experience of coping and quality of life is increased. Through our pedagogical practice , it is our ambition to meet the individual at eye level, where they are,  so that we can create a good daily structure and adapt requirements to the individual's prerequisites.</p>
