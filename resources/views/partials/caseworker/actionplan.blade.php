<div class="half-section-paragraph">
  <h3>Visitation</h3>
  <p>We encourage you to contact Gitte or Christina by telephone, as a short description of the citizen, We make an immediate assessment of whether the citizen matches the target group for our accommodation or day care.
<br><br>
We are then happy to make ourselves available for an agreed contact visit, where future residents, caseworkers and possibly family, can come and have a chat with us and see the place.
<br><br>
If we immediately assess that the citizen <b>belongs to our target group</b>, we would like to have sent relevant case material that describes the citizen's functional ability and support needs in more detail, with a view to preparing a concrete matching answer.
<br><br>
We aim to announce matching <b>responses within 1-2 business days.</b>
</p>
</div>

<div class="half-section-paragraph">
  <h3>Documentation</h3>
  <p>All previous documentation on the citizen will be gathered. From various sources such as previous homes, caretakers, medical records, theraputic records and much more.
<br><br>
This is done in order to support the citizen assesments and ensure we have a clear and precise picture of the citizen. Ensuring we can provide the support and help that is needed for them to achieve their goals and feel day-to-day improvements during their stay.
</p>
</div>

